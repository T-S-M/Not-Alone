package sosafe.tsm.httpscom.sosafe;

//@IgnoreExtraProperties
public class Chart {

    public float acceptedCount,createdCount,invitedCount;
    public Chart() {
    }

    public float getAcceptedCount() {
        return acceptedCount;
    }

    public void setAcceptedCount(float acceptedCount) {
        this.acceptedCount = acceptedCount;
    }

    public float getCreatedCount() {
        return createdCount;
    }

    public void setCreatedCount(float createdCount) {
        this.createdCount = createdCount;
    }

    public float getInvitedCount() {
        return invitedCount;
    }

    public void setInvitedCount(float invitedCount) {
        this.invitedCount = invitedCount;
    }
}
